export const supportedLocales = [
  { code: 'en', label: 'English', flag: '🇬🇧' },
  { code: 'sw', label: 'Swahili', flag: '🇰🇪' },
]

export function setI18nLocale(locale: string) {
  if (!['en', 'sw'].includes(locale)) {
    locale = 'en'
  }
  const i18nInstance = (window as any).$i18n
  if (i18nInstance) {
    i18nInstance.global.locale.value = locale
  }
  document.cookie = `app_locale=${locale};path=/;max-age=31536000`
}

export function getI18nLocale() {
  const match = document.cookie.match(/app_locale=([^;]+)/)
  return match ? match[1] : 'en'
}
